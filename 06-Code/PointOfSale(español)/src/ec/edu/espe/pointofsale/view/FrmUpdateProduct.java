    private Product product(JTextField txt1, JTextField txt2, JTextField txt3, JTextField txt4) {
        Product product;
        String name;
        String id;
        float price;
        String supplier;

        id = txt1.getText();
        name = txt2.getText();
        price = Float.parseFloat(txt3.getText());
        supplier = txt4.getText();

        product = new Product(id, name, price, supplier);
        return product;
    }

